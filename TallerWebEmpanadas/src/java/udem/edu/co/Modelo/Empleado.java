package udem.edu.co.Modelo;

/**
 *
 * @author Edwar
 */
public class Empleado {
    int id;
    String cedula;
    String nom;
    String tel;
    String estado;
    String user;

    public Empleado() {
    }

    public Empleado(int id, String cedula, String nom, String tel, String estado, String user) {
        this.id = id;
        this.cedula = cedula;
        this.nom = nom;
        this.tel = tel;
        this.estado = estado;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }
    
    
    
    
}

